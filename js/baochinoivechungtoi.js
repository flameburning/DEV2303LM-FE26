var pageSize = 6;
var pageIndex = 1;
function setItem(arr, pageIndex){
    $('#list-item1').html('');
    var start = (pageIndex > 1) ? (pageSize * (pageIndex - 2) + pageSize) : 0;
    var end = pageSize * (pageIndex - 1) + pageSize;
    for(var i = start; i < end && i< arr.length; i++ ){
        renderItem(arr[i]);
    }
}
function renderItem(obj) {
    var item =  `
                  <div class="list-1">
                       <img src="${obj.image}" alt="133">
                       <h4>${obj.title}</h4>
                       <div class="noidung24">
                           <span ><i class="far fa-clock fnt-z" style="color:#ff3d3d"></i> ${obj.date}</span>
                           <p>${obj.category}</p>
                        </div>
                   </div>`;
    $('#list-item1').append(item);
}
