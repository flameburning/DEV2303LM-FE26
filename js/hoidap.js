var pageSize = 5;
var pageIndex = 1;
function setItem(arr, pageIndex){
    $('#list-item').html('');
    var start = (pageIndex > 1) ? (pageSize * (pageIndex - 2) + pageSize) : 0;
    var end = pageSize * (pageIndex - 1) + pageSize;
    for(var i = start; i < end && i< arr.length; i++ ){
        renderItem(arr[i]);
    }
}
function renderItem(obj) {
    var item =  `<div class="image13">
                    <img src= "${obj.image}"alt="132">
                    <div class="vv1">
                        <h6>${obj.title}</h6>
                        <div class="p-span">
                            <span ><i class="far fa-clock fnt-z" style="color:#ff3d3d"></i> ${obj.date}</span>
                            <p>${obj.category}</p>
                        </div>
                    </div>
                </div>
                <hr class="dotted1">`;
    $('#list-item').append(item);
}
$(document).ready(function(){
    setItem(listItem , pageIndex);
})
var totalPage = listItem.length / pageSize;
if(listItem.length % pageSize != 0){
    totalPage++;
}

function setPage(totalPage){
    $('#listPage').html('');
    for(i = 1; i <= totalPage; i++){
        let page = `<div class="pt">
                        <span onclick="setItem2(${i})">${i}</span>
                    </div>`;
        $('#listPage').append(page);
    }
}
setPage(totalPage);
function setItem2(pageIndex){
    $('#list-item').html('');
    var start = (pageIndex > 1) ? (pageSize * (pageIndex - 2) + pageSize) : 0;
    var end = pageSize * (pageIndex - 1) + pageSize;
    for(var i = start; i < end && i< listItem.length; i++ ){
        renderItem(listItem[i]);
    }
}
